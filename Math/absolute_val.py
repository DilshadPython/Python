# Comparisons Operator
# Equal ==
# Not equal !=
# Greater than >
# Less than <
# Greater or Equal >=
# Less or Equal <=


print('\n Absolute value of -7: ')
print(abs(-7))

print('\n Round value of 3.71: ')
print(round(3.71))


print('\n Round value of 3.421: ')
print(round(3.421))


num1 = 3
num2 = 5

print(' == != > < >= <=')
print(num1  == num2)
print(num1  != num2)
print(num1  > num2)
print(num1  < num2)
print(num1  >= num2)
print(num1  <= num2)
