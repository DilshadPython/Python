message = ' Welcome to the Python '

print(message)
print('========\n')

print(message.upper())
print('========\n')

print(message.lower())
print('========\n')

print('Remove all white spaces from beginning and end of string using strip() method')
print(message.strip())
print('========\n')

print('replace all e to E')
print(message.replace('e', 'E'))
print('========\n')

print('The split() method splits a string into a list of words')
print(message.split())
print('========\n')
